export function translateEventField(text: string, language: string): string {
  if (language !== 'bn' || !text) return text
  
  const translations = eventTranslations.bn
  return translations[text as keyof typeof translations] || text
}

export function translateEventTime(time: string, language: string): string {
  return translateEventField(time, language)
}

export function translateEventLocation(location: string, language: string): string {
  if (language !== 'bn') return location
  
  let translated = location
  const translations = eventTranslations.bn
  
  // Replace each translatable part
  Object.keys(translations).forEach(key => {
    if (location.includes(key)) {
      translated = translated.replace(key, translations[key as keyof typeof translations])
    }
  })
  
  return translated
}

export function translateEventTags(tags: string[], language: string): string[] {
  if (language !== 'bn') return tags
  
  return tags.map(tag => translateEventField(tag, language))
}